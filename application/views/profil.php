            Semuanya diawali dari sebuah perjalanan antara Makassar-Bulukumba dan ia menemukan sebuah ide dalam pikirannya.

            Bripka Nurdianto S.Sos lalu memperteguh keyakinannya bahwa gagal bukan berarti selesai.

            Titik awal itu lantas membuka sebuah ruang baru dalam hidupnya yaitu tanaman hidroponik hijau yang menghampar, subur, dan menjadi sumber inspirasi bagi banyak orang “Dulu, saat persiapan tes pertempuran pada Januari 2019 lalu saya sering mengakses YouTube untuk belajar. Biasanya muncul info soal hidroponik tapi selalu saya skip. Fokus saya saat itu memang belajar hal-hal yang ada kaitannya dengan tes masuk Perwira,” kenangnya, saat ditemui Mimpi itu berhenti di sana. Tiba-tiba terlintas ingatannya pada informasi tentang hidroponik.

            “Saya mulai mencari tutorial bertani hidroponik di YouTube. Modal saya saat itu cuma satu juta rupiah. Alhamdulillah dari hal kecil bisa berkembang seperti sekarang,” Perawatannya pun sangat simpel. Bripka Nurdianto akhirnya mencoba menanam sayuran yang bebas dari pestisida dengan sistem hidroponik. Tujuannya adalah untuk memenuhi kebutuhan sendiri.

            "Alhamdulillah sekali coba tanam, langsung berhasil panen dan hasil panennya saya bagikan ke tetangga, respon tetangga pada saat itu luar biasa, awalnya coba-coba akhirnya menjadi hobi dan ada penghasilan tambahan," ungkap pria kelahiran Tugondeng 25 Februari 1985 silam itu.Jenis-jenis sayuran yang ditanam Bripka Nuryanto di antaranya adalah sawit cina, lada, sawit pagoda, dan strawberry.

            Tanaman sayuran dengan sistem hidroponik ini menggunakan media air yang diatur kadar PH dan nutrisinya.Bripka Nurdianto memulainya dengan sistem hidroponik sederhana yakni sistem rakit apung atau water culture system dan DFT atau deep flow technique.Bripka Nurdianto berharap tanaman jenis hidroponik juga bisa dibudidayakan oleh warga masyarakat dan rekan-rekannya di Polres Bulukumba untuk konsumsi pribadi, menjaga gizi keluarga dan masyarakat.

            Anak kedua dari lima bersaudara dari pasangan Tajuddin dan Hj Nurfatima ini kini kerap diundang untuk menjadi pembicara dan instruktur pelatihan terkait hidroponik.

            Bripka Nuryanto mengaku ingin mengajak lebih banyak masyarakat, terutama anak-anak muda untuk mengikuti jejaknya."Hidroponik itu pemeliharaannya simpel dan sangat praktis. Kalau tidak ada tugas piket tugas saya baru menyempatkan diri untuk ke kebun sehingga tidak mengganggu kewajiban saya sebagai aparat penegak hukum. Alhamdulillah keduanya bisa berjalan melaksanakan kewajiban sebagai aparat dan menyalurkan hobi sebagai petani hidroponik," tuturnya.

            Sejauh ini hasil tanaman hidroponik Bripka Nuryanto telah merambah pemasaran di Kabupaten Sinjai, Bantaeng, Jeneponto bahkan sampai ke kota Makassar.

